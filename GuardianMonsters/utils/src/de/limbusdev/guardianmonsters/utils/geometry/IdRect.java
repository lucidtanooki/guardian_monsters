package de.limbusdev.guardianmonsters.utils.geometry;

/**
 * Identifiable Rectangle
 *
 * Created by Georg Eckert on 23.01.16.
 */
public class IdRect
{
    public int x,y;
    public int ID;

    public IdRect(int x, int y, int ID)
    {
        this.x = x;
        this.y = y;
        this.ID = ID;
    }
}
